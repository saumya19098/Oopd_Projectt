package com.example.oopd_project;

public class Inventory
{
    String itemname;
    String Price;

    public Inventory() {
    }

    public String getItemname() {
        return itemname;
    }

    public String getPrice() {
        return Price;
    }

    public void setItemname(String itemname) {
        this.itemname = itemname;
    }

    public void setPrice(String price) {
        Price = price;
    }
}
